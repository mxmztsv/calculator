export class Calculator {

    static addition (a, b) {
        return a + b
    }

    static subtraction (a, b) {
        return a - b
    }

    static multiplying (a, b) {
        return a * b
    }

    static dividing (a, b) {
        return a / b
    }

    static power (a, b) {
        return Math.pow(a, b)
    }

}
